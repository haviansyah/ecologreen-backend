class Role extends Model
{
    use HasFactory;

    const ADMIN = 1;
    const CUSTOMER = 2;
    
    protected $guarded = [];

    /**
     * Get all of the users for the Role
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasMany
     */
